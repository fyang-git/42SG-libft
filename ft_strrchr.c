#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	unsigned char	uc;
	const char		*lastc;

	uc = (unsigned char)c;
	lastc = NULL;
	while (*s)
	{
		if (*s == uc)
			lastc = s;
		s++;
	}
	if (uc == '\0')
		return ((char *)s);
	return ((char *)lastc);
}
